import SwiftUI

public extension ShapeStyle where Self == Color {
    // MARK: -  Primary
    
    /// GoTrash primary shape style.
    static var gtPrimary: Self { .gtPrimary }
    
    /// GoTrash background shape style.
    static var gtBackground: Self { .gtBackground } 
    
    /// GoTrash card background shape style.
    static var gtCardBackground: Self { .gtCardBackground }   
    
    /// GoTrash text  shape style.
    static var gtText: Self { .gtText }
    
    /// GoTrash yellow shape style.
    static var gtYellow: Self { .gtYellow }
}
